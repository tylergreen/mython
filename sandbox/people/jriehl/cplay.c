int addfunction (int a, int b)
{
	return a + b;
}

int mulfunction (int a, int b)
{
	return a * b;
}

int add3 (int a, int b, int c)
{
  return a + b +c;
}

int mul3 (int a, int b, int c)
{
  return a * b * c;
}

int add4 (int a, int b, int c, int d)
{
  return a + b + c + d;
}

int div (int a, int b)
{
  return a / b;
}

int mod (int a, int b)
{
  return a % b;
}

